void ekf_init(float X0[N_STATES], float P_diag0[N_STATES]) {
    // set P to zeros
    for (int i=0; i<N_STATES*(N_STATES+1)/2; i++) {
        P[i] = 0;
    }

    // initialize X and P
    for (int i=0; i<N_STATES; i++) {
        X[i] = X0[i];
        P[i*(i+1)/2+i] = P_diag0[i];
    }
}

float* ekf_get_state() {
    return X;
}

void ekf_get_Pdiag(float P_diag[N_STATES]) {
    for (int i=0; i<N_STATES; i++) {
        P_diag[i] = P[i*(i+1)/2+i];
    }
}

